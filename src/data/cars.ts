export const getCarById = (cars: Car[], id: string): Car | undefined => {
  return cars.find((car) => car.id === id)
}

export const filterCarsByEra = (cars: Car[], startYear: number, endYear: number): Car[] => {
  return cars.filter((car) => car.year >= startYear && car.year <= endYear)
}

export const filterCarsByBudget = (cars: Car[], maxBudget: number): Car[] => {
  return cars.filter((car) => (car.currentValue || car.msrp || 0) <= maxBudget)
}

export const filterCarsByRarity = (cars: Car[], rarity: string): Car[] => {
  return cars.filter((car) => car.rarity === rarity)
}
